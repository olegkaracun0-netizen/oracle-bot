var stats = {};

function addStat(userId, type) {
  if (!stats[userId]) {
    stats[userId] = { total: 0, predict: 0, love: 0, work: 0, health: 0, card: 0, horoscope: 0, random: 0 };
  }
  stats[userId].total++;
  if (stats[userId][type] !== undefined) {
    stats[userId][type]++;
  }
}

