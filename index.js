function getUser(id) {
  if (!users[id]) {
    users[id] = {
      stats: { total: 0, predict: 0, love: 0, work: 0, health: 0, card: 0, horoscope: 0, random: 0 },
      points: 0,
      streak: 0,
      lastDay: null,
      achievements: [],
      birthday: null,
      mode: 'normal', // normal | angry | kind | philosopher | adult
      lang: 'ru',
    };
  }
  return users[id];
}

function addPoints(id, pts) {
  var u = getUser(id);
  u.points += pts;
  checkAchievements(id);
}

function updateStreak(id) {
  var u = getUser(id);
  var today = getTodayDate();
  if (u.lastDay === today) return;
  var yesterday = getYesterdayDate();
  if (u.lastDay === yesterday) {
    u.streak++;
  } else if (u.lastDay !== today) {
    u.streak = 1;
  }
  u.lastDay = today;
}

function checkAchievements(id) {
  var u = getUser(id);
  var earned = [];
  if (u.points >= 10 && !u.achievements.includes('newbie')) { u.achievements.push('newbie'); earned.push('🌱 Новичок — первые 10 очков!'); }
  if (u.points >= 50 && !u.achievements.includes('curious')) { u.achievements.push('curious'); earned.push('🔍 Любопытный — 50 очков!'); }
  if (u.points >= 100 && !u.achievements.includes('adept')) { u.achievements.push('adept'); earned.push('⭐ Адепт Корнелиуса — 100 очков!'); }
  if (u.points >= 300 && !u.achievements.includes('master')) { u.achievements.push('master'); earned.push('🏆 Мастер — 300 очков!'); }
  if (u.stats.predict >= 10 && !u.achievements.includes('prophet')) { u.achievements.push('prophet'); earned.push('🔮 Пророк — 10 предсказаний!'); }
  if (u.streak >= 3 && !u.achievements.includes('streak3')) { u.achievements.push('streak3'); earned.push('🔥 3 дня подряд!'); }
  if (u.streak >= 7 && !u.achievements.includes('streak7')) { u.achievements.push('streak7'); earned.push('💫 Неделя с Корнелиусом!'); }
  return earned;
}

function getTodayDate() {
  var d = new Date();
  return d.getFullYear() + '-' + (d.getMonth()+1) + '-' + d.getDate();
}

function getYesterdayDate() {
  var d = new Date();
  d.setDate(d.getDate() - 1);
  return d.getFullYear() + '-' + (d.getMonth()+1) + '-' + d.getDate();
}

