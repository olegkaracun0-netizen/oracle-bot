function getCurrency(callback) {
  https.get('https://api.exchangerate-api.com/v4/latest/USD', function(res) {
    var data = '';
    res.on('data', function(chunk) { data += chunk; });
    res.on('end', function() {
      try {
        var d = JSON.parse(data);
        var rub = d.rates.RUB ? d.rates.RUB.toFixed(2) : '?';
        var eur = d.rates.EUR ? (d.rates.RUB / d.rates.EUR).toFixed(2) : '?';
        callback('💱 *Курс валют:*\n\n🇺🇸 1 USD = *' + rub + ' ₽*\n🇪🇺 1 EUR = *' + eur + ' ₽*\n\n_Звёзды советуют: трать меньше, копи больше_ 😄');
      } catch(e) { callback('😔 Не могу получить курс. Меркурий виноват.'); }
    });
  }).on('error', function() { callback('😔 Ошибка соединения.'); });
}

