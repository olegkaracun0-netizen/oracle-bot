bot.on('text', function(ctx) {
  var text = ctx.message.text;
  var lower = text.toLowerCase().trim();
  var userId = ctx.from.id;

  // День рождения
  if (birthdayState[userId]) {
    delete birthdayState[userId];
    if (/^\d{1,2}\.\d{1,2}$/.test(text)) {
      getUser(userId).birthday = text;
      return ctx.reply('🎂 Дата рождения сохранена: ' + text + '\n\nКорнелиус запомнил! Поздравит тебя в нужный день 🎉', mainKeyboard);
    } else {
      return ctx.reply('😕 Неверный формат. Напиши ДД.ММ, например: 15.07', mainKeyboard);
    }
  }

  // Город для погоды
  if (cityWeatherState[userId]) {
    delete cityWeatherState[userId];
    ctx.reply('🌦️ Запрашиваю погоду в ' + text + '...', mainKeyboard);
    return getWeatherForCity(text, function(result, err) {
      if (err) return ctx.reply('😔 Город не найден. Попробуй написать по-английски: Moscow, Berlin', mainKeyboard);
      ctx.replyWithMarkdown(result, mainKeyboard);
    });
  }

  // Викторина
  if (userQuizState[userId] && /^[1-4]$/.test(lower)) {
    var q = userQuizState[userId];
    delete userQuizState[userId];
    var answer = parseInt(lower) - 1;
    if (answer === q.correct) {
      addPoints(userId, 3);
      return ctx.reply('✅ Правильно! 𝕮𝖔𝖗𝖓𝖊𝖑𝖎𝖚𝖘 впечатлён! +3 очка\n\n' + q.explanation, mainKeyboard);
    } else {
      return ctx.reply('❌ Неверно! Правильный ответ: ' + (q.correct+1) + '. ' + q.options[q.correct] + '\n\n' + q.explanation, mainKeyboard);
    }
  }

  // Толкование снов
  if (lower.indexOf('приснил') !== -1 || lower.indexOf('сон') !== -1 || lower.indexOf('снился') !== -1) {
    addPoints(userId, 1);
    var interp = null;
    for (var i = 0; i < dreamInterpretations.length; i++) {
      if (lower.indexOf(dreamInterpretations[i].key) !== -1) {
        interp = dreamInterpretations[i].text;
        break;
      }
    }
    if (!interp) interp = '😴 Интересный сон! 𝕮𝖔𝖗𝖓𝖊𝖑𝖎𝖚𝖘 изучил все знаки и говорит: ' + getFortune();
    return ctx.reply('🌙 Толкование сна:\n\n' + interp, mainKeyboard);
  }

  // Гороскоп
  if (horoscopes[lower]) {
    addPoints(userId, 1);
    var h = horoscopes[lower];
    return ctx.reply(h.emoji + ' ' + lower.charAt(0).toUpperCase() + lower.slice(1) + ': ' + h.text + '\n\n✨ ' + getFortune(), mainKeyboard);
  }

  // Магический шар — вопрос
  if (lower.indexOf('?') !== -1 || lower.indexOf('ли ') !== -1 || lower.indexOf('будет') !== -1 || lower.indexOf('получится') !== -1) {
    addPoints(userId, 1);
    return ctx.reply('🎱 Магический шар отвечает:\n\n' + getRandom(magicBallAnswers), mainKeyboard);
  }

  // Приветствие
  if (lower.indexOf('привет') !== -1 || lower.indexOf('хай') !== -1 || lower.indexOf('hello') !== -1) {
    var name = ctx.from.first_name || 'смертный';
    return ctx.reply('✨ Приветствую, ' + name + '!\n\n' + getFortune(), mainKeyboard);
  }

  if (lower.indexOf('спасибо') !== -1 || lower.indexOf('благодарю') !== -1) {
    return ctx.reply('🙏 Не благодари — благодари звёзды! Хотя они заняты.', mainKeyboard);
  }

  if (lower.indexOf('скучно') !== -1 || lower.indexOf('грустно') !== -1) {
    return ctx.reply('😏 Скуке приходит конец!\n\n' + getFortune() + '\n\nВселенная рекомендует: поговори с кактусом.', mainKeyboard);
  }

  // По умолчанию
  addPoints(userId, 1);
  // Редкий скрытый ответ (1 из 15)
  if (Math.random() < 0.067) {
    return ctx.reply('🌑 ' + getRandom(secretFortunes), mainKeyboard);
  }
  ctx.reply('🔮 𝕮𝖔𝖗𝖓𝖊𝖑𝖎𝖚𝖘 слышит тебя...\n\n' + getFortuneByMode(userId), mainKeyboard);
});

