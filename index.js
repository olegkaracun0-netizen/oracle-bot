function getWeatherForCity(city, callback) {
  var url = 'https://api.openweathermap.org/data/2.5/weather?q=' + encodeURIComponent(city) + '&appid=' + WEATHER_KEY + '&units=metric&lang=ru';
  https.get(url, function(res) {
    var data = '';
    res.on('data', function(chunk) { data += chunk; });
    res.on('end', function() {
      try {
        var w = JSON.parse(data);
        if (w.cod !== 200) return callback(null, w.message || 'Город не найден');
        var desc = w.weather[0].description;
        desc = desc.charAt(0).toUpperCase() + desc.slice(1);
        var temp = Math.round(w.main.temp);
        var feels = Math.round(w.main.feels_like);
        var humidity = w.main.humidity;
        var wind = Math.round(w.wind.speed);
        var pressure = Math.round(w.main.pressure * 0.750062);
        var id = w.weather[0].id;
        var icon = id >= 200 && id < 300 ? '⛈️' : id >= 300 && id < 400 ? '🌦️' : id >= 500 && id < 600 ? '🌧️' : id >= 600 && id < 700 ? '❄️' : id >= 700 && id < 800 ? '🌫️' : id === 800 ? '☀️' : id === 801 ? '🌤️' : '☁️';
        var text = icon + ' *Погода в ' + w.name + ':*\n\n🌡️ Температура: *' + temp + '°C*\n🤔 Ощущается: *' + feels + '°C*\n💧 Влажность: *' + humidity + '%*\n💨 Ветер: *' + wind + ' м/с*\n📊 Давление: *' + pressure + ' мм рт.ст.*\n☁️ ' + desc;
        callback(text, null);
      } catch(e) { callback(null, 'Ошибка'); }
    });
  }).on('error', function() { callback(null, 'Ошибка сети'); });
}

